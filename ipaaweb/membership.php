<h1 class = "membership">Benefits of membership</h1>  
 
<h3>There are a wide range of personal, professional and corporate benefits from membership of IPAA, including the following:</h3>  
 
<p>  
<ol>  
 
<li> All private members, including nominated corporate member representatives, receive the IPAA quarterly publication: the Australian Journal of Public Administration, a peer reviewed journal committed to the study and practice of public administration, public management and policy making.</li>  
<li> Lower admission fees to forums, seminars and special events.</li>  
<li> Opportunities to network with people at all levels, from other areas of government and promote yourself and your organisation. </li>  
<li> Provision of information and insights in relation to what is happening elsewhere at the Local, State and Commonwealth Government levels.</li>  
<li> Learning opportunities through the professional development program.</li>  
<li> Opportunities to share ideas contribute to the development of public sector administration in the state and build public sector management as a profession.</li>  
<li> Discover the wider public service and understand the role of other areas of the public service. </li>  
<li> Develop leadership qualities.</li>  
<li> A program of awards that recognise excellence of public sector teams, individuals and students.</li>  
<li> Share ideas and promote the value of the public service and build the profession. </li>  
 
</ol>  
</p>  
